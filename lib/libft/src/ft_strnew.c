#include "../../include/libft.h"

char	*ft_strnew(size_t size)
{
	char *str;

	if (!(size + 1))
		return (NULL);
	str = (char *)malloc(size + 1);
	if (!str)
		return (NULL);
	ft_bzero(str, size + 1);
	return (str);
}
