#include "libft.h"

void	ft_lstdel(t_list **alst, void (*del)(void *, size_t))
{
	t_list	*buf;

	buf = *alst;
	while (buf)
	{
		*alst = buf->next;
		(del)(buf->content, buf->content_size);
		buf->content = NULL;
		free(buf);
		buf = *alst;
	}
	*alst = NULL;
}
