#include "libft.h"

char	*ft_strnew(size_t size)
{
	char	*ans;

	if (!(size + 1))
		return (NULL);
	ans = (char *)malloc(sizeof(char) * (size + 1));
	if (!ans)
		return (NULL);
	ans = ft_memset(ans, '\0', size + 1);
	return (ans);
}
