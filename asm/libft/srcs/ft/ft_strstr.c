#include "libft.h"

char	*ft_strstr(const char *haystack, const char *needle)
{
	size_t	n;

	n = (size_t)ft_strlen((char *)needle);
	if (!*needle || !haystack)
		return ((char *)haystack);
	while (*haystack)
	{
		if (*haystack == *needle)
			if (!ft_memcmp(haystack, needle, n))
				return ((char *)haystack);
		haystack++;
	}
	return (0);
}
